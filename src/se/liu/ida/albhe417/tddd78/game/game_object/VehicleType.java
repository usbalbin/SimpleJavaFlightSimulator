package se.liu.ida.albhe417.tddd78.game.game_object;

/**
 * This enum might be used to specify a vehicle type
 */
public enum VehicleType {
    /**
     * AIRPLANE_BOX corresponds to AirplaneBox which is the simples AIRPLANE
     */
    AIRPLANE_BOX,

    /**
     * AIRPLANE corresponds to Airplane which is a more advanced AIRPLANE
     */
    AIRPLANE,

    /**
     * AIRPLANE_BOX corresponds to AirplaneBox which is a simple helicopter
     */
    HELICOPTER_BOX
}
